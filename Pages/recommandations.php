    <h1 class="text-center">Recommandations de la CNIL</h1>
    <h5 class="mt-5">Article 32 - Sécurité du traitement</h5>
    <p>Compte tenu de l'état des connaissances, des coûts de mise en œuvre et de la nature, de la portée, 
        du contexte et des finalités du traitement ainsi que des risques, dont le degré de probabilité et de gravité varie, 
        pour les droits et libertés des personnes physiques, le responsable du traitement et le sous-traitant 
        mettent en œuvre les mesures techniques et organisationnelles appropriées afin de garantir un niveau de sécurité 
        adapté au risque, y compris entre autres, selon les besoins :
    <ul class="list-unstyled"><li>a) la pseudonymisation et le chiffrement des données à caractère personnel;</li>
    <li>b) des moyens permettant de garantir la confidentialité, l'intégrité, la disponibilité et la résilience 
        constantes des systèmes et des services de traitement;</li>
    <li>c) des moyens permettant de rétablir la disponibilité des données à caractère personnel et l'accès à celles-ci 
        dans des délais appropriés en cas d'incident physique ou technique;</li>
    <li>d) une procédure visant à tester, à analyser et à évaluer régulièrement l'efficacité des mesures techniques et 
        organisationnelles pour assurer la sécurité du traitement.</li></ul>
    Lors de l'évaluation du niveau de sécurité approprié, il est tenu compte en particulier des risques que présente 
        le traitement, résultant notamment de la destruction, de la perte, de l'altération, de la divulgation non autorisée 
        de données à caractère personnel transmises, conservées ou traitées d'une autre manière, ou de l'accès non autorisé 
        à de telles données, de manière accidentelle ou illicite.
    L'application d'un code de conduite approuvé comme le prévoit l'article 40 ou d'un mécanisme de certification approuvé 
    comme le prévoit l'article 42 peut servir d'élément pour démontrer le respect des exigences prévues au paragraphe 1 
    du présent article.
    Le responsable du traitement et le sous-traitant prennent des mesures afin de garantir que toute personne physique 
    agissant sous l'autorité du responsable du traitement ou sous celle du sous-traitant, qui a accès à des données 
    à caractère personnel, ne les traite pas, excepté sur instruction du responsable du traitement, à moins d'y être obligée
     par le droit de l'Union ou le droit d'un État membre.</p>
    <p>Source : <a href="https://www.cnil.fr/fr/reglement-europeen-protection-donnees/chapitre4#Article32">https://www.cnil.fr/fr/reglement-europeen-protection-donnees/chapitre4#Article32</a></p>
